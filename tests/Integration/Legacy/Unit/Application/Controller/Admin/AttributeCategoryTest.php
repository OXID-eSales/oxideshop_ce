namespace OxidEsales\EshopCommunity\Tests\Unit\Application\Controller\Admin;

use OxidEsales\EshopCommunity\Application\Model\Attribute;

/**
 * Tests for Attribute_Category class
 */
class AttributeCategoryTest extends \PHPUnit\Framework\TestCase
{

    /**
     * Attribute_Category::Render() test case
     */
    public function testRender()
    {
        $this->setRequestParameter("oxid", "testId");

        // testing..
        $oView = oxNew('Attribute_Category');
        $this->assertSame('attribute_category', $oView->render());
        $aViewData = $oView->getViewData();
        $this->assertArrayHasKey('edit', $aViewData);
        $this->assertInstanceOf(\OxidEsales\EshopCommunity\Application\Model\Attribute::class, $aViewData['edit']);
    }
}
